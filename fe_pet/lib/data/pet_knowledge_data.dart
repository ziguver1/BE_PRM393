class PetKnowledgeItem {
  final String id;
  final String subject;
  final String type;
  final String name;
  final String brand;
  final String category;
  final String petType;
  final String ageRange;
  final int price;
  final int stock;
  final String status;
  final List<String> tags;
  final List<String> aliases;
  final List<String> suitableFor;
  final List<String> notSuitableFor;
  final List<String> ingredients;
  final List<String> benefits;
  final List<String> warnings;
  final String description;
  final String feedingGuide;
  final String recommendationNote;
  final String imageUrl;

  const PetKnowledgeItem({
    required this.id,
    required this.subject,
    required this.type,
    required this.name,
    required this.brand,
    required this.category,
    required this.petType,
    required this.ageRange,
    required this.price,
    required this.stock,
    required this.status,
    required this.tags,
    required this.aliases,
    required this.suitableFor,
    required this.notSuitableFor,
    required this.ingredients,
    required this.benefits,
    required this.warnings,
    required this.description,
    required this.feedingGuide,
    required this.recommendationNote,
    required this.imageUrl,
  });

  String get frontmatter {
    return '''
---
subject: $subject
type: $type
product_id: $id
name: "$name"
brand: "$brand"
category: "$category"
pet_type: "$petType"
age_range: "$ageRange"
price: "$price"
stock: "$stock"
status: "$status"
priority: primary
---
''';
  }

  String get knowledgeText {
    return '''
$frontmatter

# $name

## Product Summary
$description

## Product Metadata
Brand: $brand
Category: $category
Pet type: $petType
Age range: $ageRange
Price: $price VND
Stock: $stock
Status: $status

## Tags
${tags.join(', ')}

## Common Customer Search Terms
${aliases.join(', ')}

## Suitable For
${suitableFor.map((e) => '- $e').join('\n')}

## Not Suitable For
${notSuitableFor.map((e) => '- $e').join('\n')}

## Ingredients
${ingredients.map((e) => '- $e').join('\n')}

## Benefits
${benefits.map((e) => '- $e').join('\n')}

## Warnings
${warnings.map((e) => '- $e').join('\n')}

## Feeding Guide
$feedingGuide

## Recommendation Notes
$recommendationNote
''';
  }
}

